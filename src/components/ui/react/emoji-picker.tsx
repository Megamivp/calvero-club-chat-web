interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
}

export function EmojiPicker({ onEmojiSelect }: EmojiPickerProps) {
  const [searchTerm, setSearchTerm] = useState("");
  const [selectedCategory, setSelectedCategory] =
    useState<keyof typeof emojiCategories>("smileys");

  const filteredEmojis = searchTerm
    ? Object.values(emojiCategories)
        .flatMap((category) => category.emojis)
        .filter(() => {
          return true;
        })
    : emojiCategories[selectedCategory].emojis;

  return (
    <HoverCard>
      <Tooltip>
        <TooltipTrigger asChild>
          <HoverCardTrigger asChild>
            <Button
              type="button"
              variant="outline"
              size="icon"
              className="flex-shrink-0"
            >
              <Smile />
            </Button>
          </HoverCardTrigger>
        </TooltipTrigger>
        <TooltipContent>
          <p>Add emoji</p>
        </TooltipContent>
      </Tooltip>
      <HoverCardContent
        className="w-80 p-0"
        side="top"
        align="end"
        sideOffset={8}
      >
        <div className="flex h-96 flex-col overflow-hidden">
          <div className="border-b p-3">
            <div className="relative">
              <Search className="absolute left-3 top-1/2 h-4 w-4 -translate-y-1/2 transform text-muted-foreground" />
              <Input
                placeholder="Search emojis..."
                value={searchTerm}
                onChange={(e) => setSearchTerm(e.target.value)}
                className="h-8 pl-9"
              />
            </div>
          </div>

          {!searchTerm && (
            <div className="grid grid-cols-4 border-b">
              {Object.entries(emojiCategories).map(([key, category]) => (
                <Button
                  key={key}
                  variant={selectedCategory === key ? "default" : "ghost"}
                  size="sm"
                  className="h-8 rounded-none text-xs"
                  onClick={() =>
                    setSelectedCategory(key as keyof typeof emojiCategories)
                  }
                >
                  {category.name.split(" ")[0]}
                </Button>
              ))}
            </div>
          )}

          <ScrollArea className="min-h-0 flex-1">
            <div className="p-2">
              <div className="grid grid-cols-8 gap-1">
                {filteredEmojis.map((emoji, index) => (
                  <Tooltip key={`${emoji}-${index}`}>
                    <TooltipTrigger asChild>
                      <Button
                        variant="ghost"
                        size="sm"
                        className="h-8 w-8 p-0 hover:bg-accent"
                        onClick={() => onEmojiSelect(emoji)}
                      >
                        <span className="text-lg">{emoji}</span>
                      </Button>
                    </TooltipTrigger>
                    <TooltipContent>
                      <p>{emoji}</p>
                    </TooltipContent>
                  </Tooltip>
                ))}
              </div>
            </div>
          </ScrollArea>

          <div className="border-t p-2 text-center text-xs text-muted-foreground">
            Click an emoji to add it to your message
          </div>
        </div>
      </HoverCardContent>
    </HoverCard>
  );
}
